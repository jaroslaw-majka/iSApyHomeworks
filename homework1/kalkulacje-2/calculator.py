def srednia_arytmetyczna(num_1, num_2):
    print(f'Średnia arytmetyczna wynosi: {(num_1 + num_2) / 2} \n')


def potega(num_1, num_2):
    print(f'Potęga wynosi {num_1 ** num_2}\n')


def porownywarka(num_1, num_2):
    if num_1 != num_2:
        print(f'Liczba {max(num_1, num_2)} jest wyższa\n')
    else:
        print("Liczby są równe\n")
